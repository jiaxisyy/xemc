public class Circle {
    private int x;
    private int y;
    private int flag;
    private Boolean LINE;

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getFlag() {
        return flag;
    }

    public void setFlag(int flag) {
        this.flag = flag;
    }

    public Boolean getLINE() {
        return LINE;
    }

    public void setLINE(Boolean LINE) {
        this.LINE = LINE;
    }
}
